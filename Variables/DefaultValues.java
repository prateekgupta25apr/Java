class DefaultValues{
	static byte b;
	static short s;
	static int i;
	static long l;
	static char c;
	static float f;
	static double d;
	static boolean boo;
	
	public static void main (String[] a){
		System.out.println("Default value for byte is :"+b);
		System.out.println("Default value for short is :"+s);
		System.out.println("Default value for int is :"+i);
		System.out.println("Default value for long is :"+l);
		System.out.println("Default value for char is :"+c);
		System.out.println("Default value for float is :"+f);
		System.out.println("Default value for double is :"+d);
		System.out.println("Default value for boolean is :"+boo);
	}
}
